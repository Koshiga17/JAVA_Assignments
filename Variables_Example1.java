package com.cts;

public class Variables_Example1 {

	public static void main(String[] args) {
		int number1=12;
		int number2=13;
		int number3=11;
		System.out.println("sum of three numbers is: "+(number1+number2+number3));
		System.out.println("average of three numbers is: "+(number1+number2+number3)/3);
		System.out.println("sub of three numbers is: "+(number1-number2-number3));
		System.out.println("multiplication of three numbers is: "+(number1*number2*number3));
	}

}
